#!/usr/bin/perl

# Here, we will see another conditional statement: unless
# Unless starts executing the code written inside it when the condition is FALSE!
# So, if the condition is true, 'unless' will not execute!


# Here, we will declare a=22. In the unless condition, we check if a<20. Clealy, the condition is
# FLASE. So unless will execute the code inside it (It will print).

$a=22;
unless($a<20) {
print("Hello There!\n");
} 


# 'unless' can be thought of as 'if not'




























