# Here,we will see how can we assign values to multiple variables
# at a time


# No. of variables = No. of items

x,y,z= "Orange","Banana","Apple"
print(x)
print(y)
print(z)

# Assigning same value to multiple variables

p=q=r="Cherry"
print(p)
print(q)
print(r)


# NOTE: Unpacking a collection
#	Here, we can break and assign individual elements of a 
#	list/tuple etc to individual variables.


fruits=["Apples","Oranges","Bananas"]
s,t,u=fruits
print(s)
print(t)
print(u)




































