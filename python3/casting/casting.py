# Casting/Typecasting: Used to specify the type of variable.
#		       It can also be used to change the type of variable.

x=str(3)  	# x is a string "3"
y=int(4)	# y is an integer with value 4
z= float(5)	# z is a floating point number with value 5

print(x)
print(y)
print(z)


# We can get the type of variables by using 'type'

print(type(x))
print(type(y))
print(type(z))

























